function replaceMtl(lang) {
	var mtlContainers = $('mtl');
	var i = 0;
	while (mtlContainers[i]) {
		var str = $(mtlContainers[i]).text();
		var j = 0;
		var matched = 0;
		while ((mtl[j])&&(matched==0)) {
			if (mtl[j].title == str) {
				switch (lang) {
					case 'cz':
						$(mtlContainers[i]).text(mtl[j].cz);
						break;
					case 'en':
					default:
						$(mtlContainers[i]).text(mtl[j].en);
						break;
				}
				matched++;
			}
			j++;
		}
		i++;
	}
}

